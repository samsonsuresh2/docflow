CREATE TABLE USERACCOUNT (
    ID              NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USERNAME        VARCHAR2(100 CHAR) NOT NULL UNIQUE,
    PASSWORD_HASH   VARCHAR2(255 CHAR) NOT NULL,
    CLIENT_ID       VARCHAR2(50 CHAR),
    ROLE            VARCHAR2(50 CHAR),
    ACTIVE          NUMBER(1,0) DEFAULT 1 CHECK (ACTIVE IN (0,1))
);

COMMENT ON TABLE USERACCOUNT IS 'Application users for DocFlow login/authentication';
COMMENT ON COLUMN USERACCOUNT.USERNAME IS 'Unique login name';
COMMENT ON COLUMN USERACCOUNT.PASSWORD_HASH IS 'BCrypt-hashed password';
COMMENT ON COLUMN USERACCOUNT.ACTIVE IS '1=Active, 0=Inactive';

